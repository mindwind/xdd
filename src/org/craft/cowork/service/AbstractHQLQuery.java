package org.craft.cowork.service;



/**
 * Abstract hql query
 * 
 * @author Frank Hu
 * @version 1.0, 2009-5-18
 */
public abstract class AbstractHQLQuery implements HQLQueryService {

	protected int first = 0;

	protected int max = Integer.MAX_VALUE;

	public int getFirst() {
		return first;
	}

	public void setFirst(int firstResult) {
		this.first = firstResult;
	}

	public int getMax() {
		return max;
	}

	public void setMax(int maxResult) {
		this.max = maxResult;
	}
}
